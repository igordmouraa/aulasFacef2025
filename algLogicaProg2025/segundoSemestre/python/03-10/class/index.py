vet = [] # Inicializa um vetor vazio
for i in range(5):
    numero = int(input(f"Digite o {i+1}º número: ")) # Solicita ao usuário que digite um número
    vet.append(numero) # Adiciona o número ao vetor

print("\nVetor:")
for i in range (5):
    print(vet[i]) # Acessa cada elemento do vetor individualmente

print("\nVetor")
print(vet) # Imprime o vetor completo de uma vez

